// トーナメントDOMに状態を反映してクリックイベントをバインド
function renderBracketDOM() {
  const matchups = document.querySelectorAll(".matchup-box");
  
  matchups.forEach(box => {
    const matchId = box.dataset.matchId;
    const winnerId = state.bracket[matchId.toString()];
    const slots = box.querySelectorAll(".team-slot");
    
    slots.forEach(slot => {
      const teamId = slot.dataset.teamId;
      
      // クリックイベントの再設定
      slot.onclick = null; // リセット
      
      if (!slot.classList.contains("empty") && teamId) {
        // 勝敗に応じた見た目の設定
        if (winnerId) {
          if (winnerId === teamId) {
            slot.className = "team-slot selected";
          } else {
            slot.className = "team-slot eliminated";
          }
        } else {
          slot.className = "team-slot";
        }
        
        // クリックで勝ち上がり決定
        slot.onclick = () => {
          if (winnerId === teamId) {
            // 選択解除
            delete state.bracket[matchId.toString()];
          } else {
            // 選択設定
            state.bracket[matchId.toString()] = teamId;
          }
          saveState();
          updateBracket();
        };
      } else {
        slot.className = "team-slot empty";
      }
    });
  });
}

