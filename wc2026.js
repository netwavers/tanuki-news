// 4. グループステージの描画 & ドラッグ＆ドロップ処理
function renderGroups() {
  groupsContainer.innerHTML = "";
  Object.keys(state.groups).forEach(groupName => {
    const groupCard = document.createElement("div");
    groupCard.className = "group-card";
    groupCard.innerHTML = `<h3>GROUP - ${groupName} <span class="badge-rank">A〜L</span></h3>`;
    
    const list = document.createElement("div");
    list.className = "group-teams-list";
    list.dataset.group = groupName;
    
    state.groups[groupName].forEach((team, idx) => {
      const row = document.createElement("div");
      row.className = "team-row";
      row.draggable = true;
      row.dataset.teamId = team.id;
      row.innerHTML = `
        <span class="rank-num">${idx + 1}</span>
        <div class="team-info">
          <span class="flag-icon">${team.flag}</span>
          <span class="team-name">${team.name}</span>
        </div>
        <i class="fa-solid fa-bars drag-icon"></i>
      `;
      
      // ドラッグイベントの設定
      row.addEventListener("dragstart", (e) => {
        row.classList.add("dragging");
        e.dataTransfer.setData("text/plain", team.id);
        e.dataTransfer.setData("parent-group", groupName);
      });
      
      row.addEventListener("dragend", () => {
        row.classList.remove("dragging");
        saveState();
        update3rdPlaceList();
        updateBracket();
      });
      
      list.appendChild(row);
    });
    
    // ドロップ先としての動作設定
    list.addEventListener("dragover", (e) => {
      e.preventDefault();
      const draggingRow = document.querySelector(".team-row.dragging");
      if (!draggingRow || draggingRow.parentNode.dataset.group !== groupName) return;
      
      const nextSibling = getDragAfterElement(list, e.clientY);
      if (nextSibling == null) {
        list.appendChild(draggingRow);
      } else {
        list.insertBefore(draggingRow, nextSibling);
      }
      
      // 順位表示のリアルタイム更新
      const rows = [...list.querySelectorAll(".team-row")];
      rows.forEach((r, i) => {
        r.querySelector(".rank-num").innerText = i + 1;
      });
      
      // 状態データの更新
      const newOrder = rows.map(r => {
        return state.groups[groupName].find(t => t.id === r.dataset.teamId);
      });
      state.groups[groupName] = newOrder;
    });
    
    groupCard.appendChild(list);
    groupsContainer.appendChild(groupCard);
  });
}

// ドラッグ位置判別ヘルパー
function getDragAfterElement(container, y) {
  const draggableElements = [...container.querySelectorAll(".team-row:not(.dragging)")];
  return draggableElements.reduce((closest, child) => {
    const box = child.getBoundingClientRect();
    const offset = y - box.top - box.height / 2;
    if (offset < 0 && offset > closest.offset) {
      return { offset: offset, element: child };
    } else {
      return closest;
    }
  }, { offset: Number.NEGATIVE_INFINITY }).element;
}

